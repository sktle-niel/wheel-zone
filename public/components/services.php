    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const showMoreText = document.getElementById('show-more-text');
            const showMoreContainer = document.getElementById('show-more-container');
            const allHiddenCards = document.querySelectorAll('.service-card.hidden');
            allHiddenCards.forEach(card => card.style.display = 'none');
            let shownCount = 0;
            const batchSize = 6;

            function repositionShowMore() {
                const visibleCards = document.querySelectorAll('.service-card:not(.hidden)');
                const lastVisibleCard = visibleCards[visibleCards.length - 1];
                if (lastVisibleCard) {
                    lastVisibleCard.parentNode.insertBefore(showMoreContainer, lastVisibleCard.nextSibling);
                }
            }

            showMoreText.addEventListener('click', function() {
                if (showMoreText.textContent === 'Show Less') {
                    // Hide all shown cards except the initial 6
                    const shownCards = document.querySelectorAll('.service-card:not(.hidden):not(:nth-child(-n+6))');
                    shownCards.forEach(card => {
                        card.classList.add('fading');
                        setTimeout(() => {
                            card.classList.add('hidden');
                            card.style.display = 'none';
                            card.classList.remove('fading');
                        }, 500);
                    });
                    showMoreText.textContent = 'Show More';
                    shownCount = 0;
                    showMoreContainer.style.display = 'block';
                    setTimeout(repositionShowMore, 500);
                } else {
                    // Show next batch of 6 cards
                    const remainingHidden = Array.from(allHiddenCards).filter(card => card.classList.contains('hidden'));
                    const toShow = remainingHidden.slice(0, batchSize);
                    toShow.forEach(card => {
                        card.classList.remove('hidden');
                        card.style.display = 'block';
                        card.classList.add('fading');
                        setTimeout(() => {
                            card.classList.remove('fading');
                        }, 500);
                    });
                    shownCount += toShow.length;
                    const newRemainingHidden = Array.from(allHiddenCards).filter(card => card.classList.contains('hidden'));
                    if (newRemainingHidden.length === 0) {
                        showMoreText.textContent = 'Show Less';
                    } else {
                        showMoreText.textContent = 'Show More';
                    }
                    setTimeout(repositionShowMore, 500);
                }
            });

            // Initial reposition
            repositionShowMore();
        });
    </script>
